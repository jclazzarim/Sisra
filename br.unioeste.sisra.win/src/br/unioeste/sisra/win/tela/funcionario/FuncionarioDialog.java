    @Override
    public void preencherDadosInciais(Object objeto) {
        if (objeto != null && objeto instanceof FuncionarioTO) {
            FuncionarioTO f = (FuncionarioTO) objeto;
            idFuncionario = f.getId();
            txtNome.setText(f.getNome());
            txtCpf.setText(f.getCpf());
            txtCidade.setText(f.getCidade());
            txtRua.setText(f.getRua());
            txtBairro.setText(f.getBairro());
            txtNumero.setText(String.valueOf(f.getNumero()));
            try {
                jxdpContratacao.setDate(f.getDtContratacao());
                jxdpDemissao.setDate(f.getDtDemissao());
                jxdpNascimento.setDate(f.getDtNascimento());
            } catch (Exception ex) {
                Logger.getLogger(FuncionarioDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jcbUF.setSelectedItem(f.getEstado());
            jcbPais.setSelectedItem(f.getPais());
            txtRG.setText(f.getRg());
            
            if(f.getSexo()){
                jcbSexo.setSelectedIndex(0);//homem true
            }else{
                jcbSexo.setSelectedIndex(1);//mulher false
            }
            
        }
    }

    @Override
    public void configurarHandler() {
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_NOME, txtNome);
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_CPF, txtCpf);
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_RUA, txtRua);
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_CIDADE, txtCidade);
        
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_DT_NASCIMENTO, jxdpNascimento);
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_DT_CONTRATO, jxdpContratacao);
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_DT_DEMISSAO, jxdpDemissao);
        handler.add(FuncionarioValidacao.CHAVE_CAMPO_FUNCIONARIO_NUMERO_RESIDENCIA, txtNumero);
    }

    @Override
    public FuncionarioTO getDados() {

        FuncionarioTO f = new FuncionarioTO();
        if (!novo) {
            f.setId(idFuncionario);
        }
        f.setNome(txtNome.getText());
        f.setCpf(txtCpf.getText());
        f.setRua(txtRua.getText());
        f.setCidade(txtCidade.getText());
        f.setBairro(txtBairro.getText());
        try {
            f.setDtContratacao(jxdpContratacao.getDate());
            f.setDtDemissao(jxdpDemissao.getDate());
            f.setDtNascimento (jxdpNascimento.getDate());
        } catch (Exception ex) {
            Logger.getLogger(FuncionarioDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        f.setEstado((String)jcbUF.getSelectedItem());
        f.setPais((String) jcbPais.getSelectedItem());
        f.setRg(txtRG.getText());
        f.setSexo(jcbSexo.getSelectedIndex() == 0 ? true : false);
        f.setNumero(txtNumero.getText());
        
        
        return f;
    }
