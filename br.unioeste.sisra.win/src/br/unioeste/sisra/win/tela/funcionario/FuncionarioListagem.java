    private void initTabelaFuncionarios() {
        String[] colunas = new String[4];
        ArrayList dadosTabela = new ArrayList();

        colunas[FuncionarioActivity.Campo.ID] = "Id";
        colunas[FuncionarioActivity.Campo.NOME] = "Nome";
        colunas[FuncionarioActivity.Campo.CPF] = "CPF";
        colunas[FuncionarioActivity.Campo.RG] = "RG";


        tblFuncionariosModel = new SimpleTableModel(dadosTabela, colunas);
    }

    public void exibeBusca(FuncionarioTO[] funcionarios) {
        tblFuncionariosModel.removeAllElements();

        for (FuncionarioTO f : funcionarios) {
            tblFuncionariosModel.addRow(toArrayString(f));
        }
        tblFuncionarios.updateUI();
    }

    private void configurarHandler() {
        handler.add(Validacao.CHAVE_CAMPO_BUSCA, txtBusca);
    }

    public void atualizaFuncionario(FuncionarioTO f) {
        final int idLinha = tblFuncionariosModel.getIdLinha(FuncionarioActivity.Campo.ID, String.valueOf(f.getId()));
        if (idLinha >= 0) {
            tblFuncionariosModel.editarRow(idLinha, toArrayString(f));
        }
        tblFuncionarios.updateUI();
    }

    public void remover(String pk) {
        tblFuncionariosModel.removeRow(pk, FuncionarioActivity.Campo.ID);
        tblFuncionarios.updateUI();
    }

    private String[] toArrayString(FuncionarioTO f) {
        String[] linha = new String[5];
        linha[FuncionarioActivity.Campo.ID] = f.getId() == null ? "" : String.valueOf(f.getId());
        linha[FuncionarioActivity.Campo.NOME] = f.getNome();
        linha[FuncionarioActivity.Campo.CPF] = f.getCpf();
        linha[FuncionarioActivity.Campo.RG] = f.getRg();
        return linha;
    }
